public class Hotel {
    // attributes
    private long roomNo;
    private boolean isOccupied;

    // getters and setters
    public void setroomNo(long roomNo){
        this.roomNo = roomNo;
    }
    public void setisOccupied(boolean isOccupied){
        this.isOccupied = isOccupied;
    }
    public long getroomNo(){
        return roomNo;
    }
    public boolean getisOccupied(){
        return isOccupied;
    }
    

    
}
