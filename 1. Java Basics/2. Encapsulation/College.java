public class College {
    // attributes
    private String name;
    private String university;

    // getters and setters
    public void setname(String name){
        this.name  = name;
    }
    public void setuniversity(String university){
        this.university = university;
    }
    public String getname(){
        return name;
    }
    public String getuniversity(){
        return university;
    }
    
}
