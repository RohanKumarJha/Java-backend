public class Car {
    // attributes
    private String brand;
    private double price;
    
    // Getters and Setters
    public void setbrand(String brand){
        this.brand = brand;
    }
    public void setprice(double price){
        this.price = price;
    }
    public String getbrand(){
        return brand;
    }
    public double getprice(){
        return price;
    }
}
