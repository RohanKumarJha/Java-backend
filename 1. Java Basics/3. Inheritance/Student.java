public class Student {
    // attributes
    private String name;
    private long rollNo;
    private long marks;
    
    // Constructor 
   public void setname(String name){
    this.name = name;
   }
   public void setrollNo(long rollNo){
    this.rollNo = rollNo;
   }
   public void setmarks(long marks){
    this.marks = marks;
   }
   public String getname(){
    return name;
   }
   public long getrollNo(){
    return rollNo;
   }
   public long getmarks(){
    return marks;
   }
}